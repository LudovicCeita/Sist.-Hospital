    public void preencherMedicos() {
        conex.conexao();
        conex.executaSql("select *from medicos order by nome_medico");
        try {
            conex.rs.first();
            jComboBoxMedico.removeAllItems();
            do {
                jComboBoxMedico.addItem(conex.rs.getString("nome_medico"));
            } while (conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher Medicos" + ex);

        }
        conex.desconecta();
    }

    public void preencheTabela(String Sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "Nome", "Turno", "Date", "Status", "Medico"};
        conex.conexao();
        conex.executaSql(Sql);
        try {
            conex.rs.first();
            do {
                dados.add(new Object[]{conex.rs.getInt("agenda_cod"), conex.rs.getString("paci_nome"), conex.rs.getString("agenda_turno"), conex.rs.getString("agenda_data"), conex.rs.getString("agenda_status"), conex.rs.getString("nome_medico")});

            } while (conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Nao Existe agendamento Para este Medico . " + ex.getMessage());
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTablePacienteAgen.setModel(modelo);
        jTablePacienteAgen.getColumnModel().getColumn(0).setPreferredWidth(30);//coluna e largura da Col = 23
        jTablePacienteAgen.getColumnModel().getColumn(0).setResizable(false);
        jTablePacienteAgen.getColumnModel().getColumn(1).setPreferredWidth(220);//coluna e largura da Col = 23
        jTablePacienteAgen.getColumnModel().getColumn(1).setResizable(false);
        jTablePacienteAgen.getColumnModel().getColumn(2).setPreferredWidth(80);//coluna e largura da Col = 23
        jTablePacienteAgen.getColumnModel().getColumn(2).setResizable(false);
        jTablePacienteAgen.getColumnModel().getColumn(3).setPreferredWidth(80);//coluna e largura da Col = 23
        jTablePacienteAgen.getColumnModel().getColumn(3).setResizable(false);
        jTablePacienteAgen.getColumnModel().getColumn(4).setPreferredWidth(120);//coluna e largura da Col = 23
        jTablePacienteAgen.getColumnModel().getColumn(4).setResizable(false);
        jTablePacienteAgen.getColumnModel().getColumn(5).setPreferredWidth(120);//coluna e largura da Col = 23
        jTablePacienteAgen.getColumnModel().getColumn(5).setResizable(false);
        jTablePacienteAgen.getTableHeader().setReorderingAllowed(false);
        jTablePacienteAgen.setAutoResizeMode(jTablePacienteAgen.AUTO_RESIZE_OFF);
        jTablePacienteAgen.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
    }

